package Methods;

public class Conversion {

    int meterToCentimeter(int a) {
        return a * 100;
    }

    int meterToMillimeter(int a) {
        return a * 1000;
    }

    double centimeterToMeter(int a) {
        return a / 100.0;
    }

    double millimeterToMeter(int a) {
        return a / 1000.0;
    }
}
